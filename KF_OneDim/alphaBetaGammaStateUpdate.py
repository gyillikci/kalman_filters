def alphaBetaGammaStateUpdate(dt, x_prev, v_prev, a_prev, x_meas, alpha, beta, gamma=0):
    """
    This is a generic function.
    This function performs the current state estimation based on the previous state prediction.

    Inputs
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------
    Variable      Variable Type       Units       Description
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------
    dt            scalar              seconds     time between samples
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------
    x_prev        scalar              meters      The previous state estimated position
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------
    v_prev        scalar              m/s         The previous state estimated velocity
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------
    a_prev        scalar              m/s^2       The previous state estimated acceleration
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------
    x_meas        scalar              meters      The measured position
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------
    alpha         scalar                          The alpha factor
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------
    beta          scalar                          The beta factor
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------
    gamma         scalar                          The gamma factor (optional parameter)
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------


    Outputs
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------
    Variable      Variable Type       Units       Description
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------
    x_cur         scalar              meters      The current state position prediction
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------
    v_cur         scalar              m/s         The current state velocity prediction
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------
    a_cur         scalar              m/s^2       The current state acceleration prediction
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------

    """

    x_cur = x_prev + alpha * (x_meas - x_prev)
    v_cur = v_prev + beta * (x_meas - x_prev) / dt
    a_cur = a_prev + gamma * (x_meas - x_prev) / (0.5 * dt**2)

    return x_cur, v_cur, a_cur
