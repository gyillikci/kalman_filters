def motionDynamicModel(dt, x_cur, v_cur, a_cur=0):
    """
    This is a generic function.
    This function performs the next state prediction based on the Newton mechanics motion equations.

    Inputs
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------
    Variable      Variable Type       Units       Description
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------
    dt            scalar              seconds     time between samples
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------
    x_cur         scalar              meters      The current state estimated position
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------
    v_cur         scalar              m/s         The current state estimated velocity
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------
    a_cur         scalar              m/s^2       The current state estimated acceleration
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------


    Outputs
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------
    Variable      Variable Type       Units       Description
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------
    x_pred        scalar              meters      The next state estimated position (prediction)
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------
    v_prd         scalar              m/s         The next state estimated velocity (prediction)
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------
    a_prd         scalar              m/s^2       The next state estimated acceleration (prediction)
    """
    x_pred = x_cur + v_cur*dt + 0.5*a_cur*dt**2
    v_prd  = v_cur + a_cur*dt
    a_prd  = a_cur

    return x_pred, v_prd, a_prd
