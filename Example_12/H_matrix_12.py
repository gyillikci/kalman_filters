def H_matrix_12(x, Hargs):
    """
    Description
    This function creates H matrix (Observation Matrix) for example 12.

    Inputs
    -------------------------------------------------------------------------------------------------------------------------------------------------------
    Variable      Variable Type       Units       Description
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------
    x             matrix                          pendulum angle and angular velocity
                                                 (first row is the pendulum angle, second row is the pendulum angular velocity)
    -------------------------------------------------------------------------------------------------------------------------------------------------------
    Hargs[0]                                      Pendulum string length
    -------------------------------------------------------------------------------------------------------------------------------------------------------

    Outputs
    -------------------------------------------------------------------------------------------------------------------------------------------------------
    Variable      Variable Type       Units       Description
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------
    H             matrix                          Observation Function Matrix
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------
    dH            matrix                          Derivative of H
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------

    """

    import numpy as np

    L = Hargs[0]

    Hx = L * np.sin(x[0])
    dHx = np.array([[L * np.cos(x[0]), 0]], dtype='float64')  # derivative of Hx

    return Hx, dHx
