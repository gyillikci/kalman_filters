def sigmaTransform(S, weights, N = 0):
    """
    Description
    this function performs Sigma Points Transform by computing transformed mean and covariance matrix

    Inputs
    -------------------------------------------------------------------------------------------------------------------------------------------------------
    Variable      Variable Type       Description
    -------------------------------------------------------------------------------------------------------------------------------------------------------
    S             matrix              Sigma Points
    -------------------------------------------------------------------------------------------------------------------------------------------------------
    weights       dict                Unscented Transform Weights
    -------------------------------------------------------------------------------------------------------------------------------------------------------
    N             matrix              Additional noise that is added to the transformed covariance matrix.
                                      It can be a Process Noise Matrix or a Measurement Noise Matrix.
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------


    Outputs
    -------------------------------------------------------------------------------------------------------------------------------------------------------
    Variable      Variable Type       Description
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------
    x       	    vector              Transformed mean
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------
    P       	    matrix              Transformed covariance matrix
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------

    """

    x = S @ weights["w_m"]
    S = S - x
    P = S @ weights["w_c"] @ S.T

    P = P + N

    return x, P