def H_matrix_14(x, Hargs):
    """
    Description
    This function creates H matrix (Observation Matrix) for example 14.

    Inputs
    -------------------------------------------------------------------------------------------------------------------------------------------------------
    Variable      Variable Type       Units       Description
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------
    x             matrix                          pendulum angle and angular velocity
                                                 (first row is the pendulum angle, second row is the pendulum angular velocity)
    -------------------------------------------------------------------------------------------------------------------------------------------------------
    Hargs[0]                                      Pendulum string length
    -------------------------------------------------------------------------------------------------------------------------------------------------------

    Outputs
    -------------------------------------------------------------------------------------------------------------------------------------------------------
    Variable      Variable Type       Units       Description
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------
    H             matrix                          Observation Function Matrix for a given x
    ------------------------------------------------------------------------------------------------------------------------------------------------------------------

    """

    import numpy as np

    L = Hargs[0]

    Hx = L * np.sin(x[np.newaxis, 0, :])

    return Hx
